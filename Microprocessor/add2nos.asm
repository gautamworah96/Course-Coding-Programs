.MODEL SMALL
.STACK 100H
.DATA
	MSG1 DB 0DH,0AH,"ENTER THE FIRST NUMBER: $"
	MSG2 DB 0DH,0AH,"ENTER THE SECOND NUMBER: $"
	MSG3 DB 0DH,0AH,"ANSWER IS: $"
	ANSWER DB 255 DUP(?)
.CODE
	START:
		MOV AX,@DATA
		MOV DS,AX
		LEA DX,MSG1
		MOV AH,09H
		INT 21H
		MOV AH,01H
		
		INT 21H
		AND AL,0FH
		SHL AL,4
		MOV BH,AL
		
		INT 21H
		AND AL,0FH
		ADD BH,AL
		MOV BL,BH

		LEA DX,MSG2
		MOV AH,09H
		INT 21H

		MOV AH,01H
		INT 21H
		AND AL,0FH
		SHL AL,4
		MOV CH,AL
		
		INT 21H
		AND AL,0FH
		ADD CH,AL
		MOV AL,CH
	
		ADD AL,BL
		DAA
		;MOV AL,AH
		;ADD AL,'0'
		;LEA SI,ANSWER
		;MOV [SI],AL
		;INC SI
		;MOV [SI],'$'

                MOV AH,09H
		LEA DX,MSG3
		INT 21H
		

		JNC DISPLAY
		LEA SI,ANSWER
		MOV [SI],'1'
		INC SI
		MOV [SI] , '$'
                MOV AH,09H
		LEA DX,ANSWER
		INT 21H


		DISPLAY:
		MOV BL,AL
		MOV DL,AL
		AND DL,0F0H
		SHR DL,4
		ADD DL,30H
		MOV AH,02H
		INT 21H
		MOV DL,BL
		AND DL,0FH
		ADD DL,30H
		INT 21H

		;LEA DX,ANSWER
		;INT 21H
		 
		MOV AH,4CH
		INT 21H
END START	